async function main() {
  console.log(`\n🇺🇸  Seeding ${BUSINESSES.length} US businesses into Peptide Alliance…\n`);

  let inserted = 0;
  let skipped  = 0;

  for (const biz of BUSINESSES) {
    const city   = biz.city   ?? 'Online';
    const state  = biz.state  ?? 'US';
    const record = {
      name:              biz.name,
      slug:              slugify(biz.name, city),
      category:          biz.category,
      subcategory:       biz.subcategory ?? '',
      city,
      city_slug:         cityToSlug(city),
      province:          state,          // province column stores state too
      address:           biz.address    ?? null,
      phone:             biz.phone      ?? null,
      website:           biz.website    ?? null,
      instagram:         biz.instagram  ?? null,
      description_en:    biz.description ?? '',
      keywords:          biz.keywords   ?? [],
      is_active:         true,
      is_verified:       false,
      is_premium:        false,
      subscription_tier: 'free',
      source:            'google_places',
      claimed_by:        null,
    };

    const { error } = await supabase.from('businesses').insert(record);

    if (error) {
      console.error(`  ✗  ${biz.name} — ${error.message}`);
      skipped++;
    } else {
      console.log(`  ✓  ${biz.name} (${city}, ${state})`);
      inserted++;
    }
  }

  console.log(`\n─────────────────────────────────────────`);
  console.log(`  Inserted : ${inserted}`);
  console.log(`  Skipped  : ${skipped}`);
  console.log(`─────────────────────────────────────────\n`);
}

main().catch((err) => {
  console.error('Fatal error:', err);
  process.exit(1);
});
